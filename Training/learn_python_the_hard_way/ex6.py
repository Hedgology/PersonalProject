
# Assigns the int 10 to a variable "types of people"
types_of_people = 10

# Embeds the variable inside the string while assigning the string to a variable "x"
x = f"There are {types_of_people} types of people."

# Assigns the string "binary" to the variable of the same name
binary = "binary"

# Assigns the word "don't" to the variable "do_not"
do_not = "don't"

# Embeds the variables "binary" and "do_not" inside of a string and assigns it to a variable
y = f"Those who known {binary} and those who {do_not}"

# Prints both varables x and y
print(x)
print(y)

# Embedds both variables 'x' and'y' inside of a string and prints
